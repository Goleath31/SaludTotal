package dtos;

/**
 *
 * @author golea
 */
public class AnalisisDTO {

    private Long id;
    private String nombre;

    public AnalisisDTO(Long id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Long getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return this.nombre;
    }
}
