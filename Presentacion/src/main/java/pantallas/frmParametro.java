    private void cargarUnidades() {
        try {
            unidadesCargadas = unidadMedidaService.obtenerTodas();
            cmbMuestras.removeAllItems();
            for (UnidadMedidaDTO u : unidadesCargadas) {
                cmbMuestras.addItem(u.getNombre());
            }
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public String getNombreParametroIngresado() {
        return txtNmbAnalisis.getText();
    }

    public int getSiguienteOrdenRango() {
        return rangosTemp.size() + 1;
    }

    public void agregarRango(RangoDTO rango) {
        rangosTemp.add(rango);
        modeloRangos.addRow(new Object[]{rango.getOrden(), rango.getEtiqueta(), rango.getRangoFormateado(), rango.getEdadInicial() + "-" + rango.getEdadFinal(), rango.getSexo()});
    }

