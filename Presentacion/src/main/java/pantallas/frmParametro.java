    private void btnAgrParametroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgrParametroActionPerformed
        // TODO add your handling code here:
        if (txtNmbAnalisis.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Primero escribe el nombre del parámetro.", "Falta información", JOptionPane.WARNING_MESSAGE);
            return;
        }
        frmRango frm = new frmRango(this);
        frm.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnAgrParametroActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
         if (txtNmbAnalisis.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre del parámetro es obligatorio.", "Falta información", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (cmbMuestras.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(this, "Selecciona la unidad de medida.", "Falta información", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (rangosTemp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Agrega al menos un rango.", "Falta información", JOptionPane.WARNING_MESSAGE);
            return;
        }

        UnidadMedidaDTO unidad = unidadesCargadas.get(cmbMuestras.getSelectedIndex());

        ParametroDTO dto = new ParametroDTO(
                txtNmbAnalisis.getText().trim(),
                padre.getSiguienteOrdenParametro(),
                txtNota.getText().trim(),
                unidad.getId(),
                unidad.getNombre()
        );
        dto.setRangos(rangosTemp);

        padre.agregarParametro(dto);
        padre.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

