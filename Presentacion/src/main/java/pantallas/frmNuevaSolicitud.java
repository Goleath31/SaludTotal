    private void cargarDoctores() {
        try {
            doctoresCargados = doctorService.obtenerDoctores();
            cmbMedicos.removeAllItems();
            for (DoctorDTO d : doctoresCargados) {
                cmbMedicos.addItem(d.getNombreCompleto());
            }
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void cargarCatalogo() {
        try {
            catalogoCargado = analisisService.obtenerCatalogo(1, 1000);
            llenarTablaPruebas(catalogoCargado);
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void llenarTablaPruebas(List<AnalisisDTO> lista) {
        modeloPruebas.setRowCount(0);
        for (AnalisisDTO dto : lista) {
            modeloPruebas.addRow(new Object[]{dto.getNombre(), dto.getCodigo()});
        }
    }
    
    private void alternarSeleccionFila() {
        int fila = tblPruebas.getSelectedRow();
        if (fila < 0 || fila >= catalogoCargado.size()) {
            return;
        }

        AnalisisDTO dto = catalogoCargado.get(fila);

        AnalisisDTO existente = null;
        for (AnalisisDTO s : seleccionados) {
            if (s.getId().equals(dto.getId())) {
                existente = s;
                break;
            }
        }

        if (existente != null) {
            seleccionados.remove(existente);
        } else {
            seleccionados.add(dto);
        }

        actualizarLabelSeleccionados();
    }

    private void actualizarLabelSeleccionados() {
        if (seleccionados.isEmpty()) {
            lblSeleccionados.setText("Seleccionados");
            return;
        }

        StringBuilder texto = new StringBuilder();
        for (AnalisisDTO dto : seleccionados) {
            if (texto.length() > 0) {
                texto.append(", ");
            }
            texto.append(dto.getNombre());
        }
        lblSeleccionados.setText(texto.toString());
    }
