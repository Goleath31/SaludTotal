    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtFolio.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El folio es obligatorio.", "Falta información", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (seleccionados.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona al menos una prueba del catálogo.", "Falta información", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            SolicitudDTO dto = new SolicitudDTO();
            dto.setFolio(txtFolio.getText().trim());
            dto.setClienteId(cliente.getId());

            if (cmbMedicos.getSelectedIndex() >= 0 && cmbMedicos.getSelectedIndex() < doctoresCargados.size()) {
                dto.setDoctorId(doctoresCargados.get(cmbMedicos.getSelectedIndex()).getId());
            }

            List<Long> ids = new ArrayList<>();
            for (AnalisisDTO a : seleccionados) {
                ids.add(a.getId());
            }
            dto.setAnalisisIds(ids);

            solicitudService.guardarSolicitud(dto);

            JOptionPane.showMessageDialog(this, "Solicitud generada correctamente.");
            if (padre != null) {
                padre.cargarSolicitudes();
                padre.setVisible(true);
            }
            this.dispose();
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error al guardar", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

