    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed

        String filtro = txtbuscador.getText().toLowerCase().trim();

        // 1. Obtener todos los datos nuevamente (o usar una lista cacheada)
        PruebaService service = new PruebaService();
        List<dtos.ClientePruebaCompletaDTO> listaCompleta = service.obtenerClientesCompletos();

        // 2. Crear un nuevo modelo para el JList
        javax.swing.DefaultListModel<String> modeloFiltrado = new javax.swing.DefaultListModel<>();

        // 3. Filtrar
        for (dtos.ClientePruebaCompletaDTO c : listaCompleta) {
            // Comparamos el folio o el nombre con el texto del buscador
            if (c.getFolio().toLowerCase().contains(filtro)
                    || c.getNombreCliente().toLowerCase().contains(filtro)) {

                modeloFiltrado.addElement(c.getFolio() + " - " + c.getNombreCliente());
            }
        }

        // 4. Asignar el modelo filtrado al JList
        jList1.setModel(modeloFiltrado);

        // Validar si no se encontraron resultados
        if (modeloFiltrado.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontraron coincidencias.");
        }

    }//GEN-LAST:event_btnbuscarActionPerformed

