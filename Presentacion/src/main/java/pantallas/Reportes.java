    private void btnprevisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprevisualizarActionPerformed
        String seleccion = jList1.getSelectedValue();
        if (seleccion == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione un cliente de la lista.");
            return;
        }

        String folio = seleccion.split(" - ")[0];
        servicios.ReporteService service = new servicios.ReporteService();
        dtos.ReportePacienteDTO datos = service.generarReporte(folio);

        if (datos != null) {
            try {
                // 1. Construir el HTML
                String html = generarHTMLReporte(datos);

                // 2. Definir ruta de salida
                String nombreArchivo = "Reporte_" + folio + ".pdf";

                // 3. Convertir a PDF
                try (java.io.OutputStream os = new java.io.FileOutputStream(nombreArchivo)) {
                    com.openhtmltopdf.pdfboxout.PdfRendererBuilder builder = new com.openhtmltopdf.pdfboxout.PdfRendererBuilder();
                    builder.withHtmlContent(html, "/");
                    builder.toStream(os);
                    builder.run();
                }

                // 4. Abrir automáticamente
                java.awt.Desktop.getDesktop().open(new java.io.File(nombreArchivo));

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al generar PDF: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnprevisualizarActionPerformed


